package Gun017;

public class JavaNestedLoop5 {
    public static void main(String[] args) {
        // carpim tablosunu ekrana yazdirin

        for (int i=1; i<10; i++){
            for (int j=1; j<10;j++){
                System.out.println(i+ "x " + j+ " = " +(i*j));
            }
            System.out.println();
        }


    }
}


