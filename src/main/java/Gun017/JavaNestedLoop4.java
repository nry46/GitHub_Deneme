package Gun017;

public class JavaNestedLoop4 {
    public static void main(String[] args) {
        // asagidaki sekilde cikti veren programi print icerisinde sadece bir tane # kullanilarak yazdirin.
        // ####
        // ####
        // ####
        // ####
        // ####

        for (int i=1; i<=5; i++)
        {

            for (int j = 1; j <= 5; j++)
            {
                System.out.print("#");


            }


            System.out.println();

        }


    }
}
