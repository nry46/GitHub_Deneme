package Gun007;

public class StringSubstring {
    public static void main(String[] args) {

        // Stringin belli bir bolumunu alma islemi
        String text = "Merhaba arkadaslar";
        System.out.println(" 1. bölüm = " + text.substring(1,5)); // 1 nolu indexten 5 ekadar . 5 dahil degil

        System.out.println("2. bölüm = " + text.substring(3,8));
        System.out.println("3. bölüm  = " + text.substring(6) ); // verilen indexten sona kadar aliyor.















    }
}
