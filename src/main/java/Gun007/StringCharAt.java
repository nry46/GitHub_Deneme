package Gun007;

public class StringCharAt {

    public static void main(String[] args) {

        // istenen noktadaki harfi verir. karakterlerin sirasi 0 dan baslar. buna index denir.

        String kelime = "Merhaba Dunya";
        char harf = kelime.charAt(3);
        System.out.println("3.harf = " + harf); // 3 ->h

        harf = kelime.charAt(0);

        System.out.println("0.harf = " + harf);









        

    }
}
